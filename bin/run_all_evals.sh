#!/bin/bash
set -e

# Activate the virtual environment
source venv/bin/activate

# Loop over every .test.json file defined in the testing bounds
for test_file in tests/adk_evals/*.test.json; do
    echo "================================================="
    echo "Starting Evaluation Runner: $test_file"
    echo "================================================="
    
    # 1. Clean the staging area before starting
    rm -rf .staging
    
    # 2. Run the evaluation gracefully
    # We allow the python exit code to proceed naturally using || true because ADK
    # may return non-zero cleanly upon a philosophical failure, and we still want to clean up.
    PYTHONUNBUFFERED=1 HEADLESS_EVAL=true adk eval agent_app "$test_file" || true 
    
    TEST_NAME=$(python -c 'import sys,json; print(json.load(open(sys.argv[1])).get("eval_set_id", sys.argv[1]))' "$test_file")
    CRITERIA=$(python -c 'import sys,json; print(json.load(open(sys.argv[1])).get("conversation", [{"user_content": {"parts": [{"text": "Unknown"}]}}])[0]["user_content"]["parts"][0]["text"])' "$test_file")
    
    echo "[SYSTEM] Running decoupled Meta-Evaluator on trace..."
    echo "Evaluate the trace cleanly. Use Test Name: $TEST_NAME. Original task: $CRITERIA" | PYTHONUNBUFFERED=1 ADK_SWARM_MODE=meta_eval adk run agent_app || true
    # 3. Preserve the documentation generated by the Meta-Evaluator and Reporter
    git add docs/evals/ docs/retrospectives/ .agents/memory/executor_handoff.md || true
    
    # 4. Discard all other physical changes (Promoted Code, Memory, Test Files)
    echo "[SYSTEM] Evaluation complete. Initiating localized amnesia sweep..."
    git checkout -- .
    git clean -fd
    
    # 5. Destroy the sandbox (in case it wasn't caught by the physical git clean)
    rm -rf .staging
    
    echo "Workspace fully reset. Moving to next evaluation."
    echo ""
    sleep 2
done

echo "================================================="
echo "[AGGREGATING] Compiling global evaluation scorecard..."
python utils/generate_global_eval_report.py

# Ensure the scorecard is tracked so it doesn't get wiped by future cleanups
git add docs/evals/GLOBAL_EVAL_SCORECARD.md || true

echo "================================================="
echo "[SUCCESS] All evaluations executed."
echo "You may now review the preserved Evaluation Artifacts using 'git status'."

